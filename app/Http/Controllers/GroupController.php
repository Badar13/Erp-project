        'group' => request()->get('group'),
        'group_code' => request()->get('group_code'),
        'detail' => request()->get('detail'),
        'status' => 'DEACTIVE', 
        ]);
        //redirect the user and send friendly message
        return redirect()->route('group.index')->with('success','Group made  successfully ');
